    public function downloadPDF($id){
        $timetable = DB::table('timetable')->where('year',$id)->get();
        $pdf = PDF::loadView('pdf', compact('timetable'))->setPaper('A4','landscape');
        return $pdf->download('Timetable.pdf');
    }
